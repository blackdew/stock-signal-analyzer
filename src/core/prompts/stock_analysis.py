"""
Stock Analysis Prompt

개별 종목 분석을 위한 LLM 프롬프트 템플릿.
6개 카테고리 점수와 상세 분석을 생성합니다.
"""

from typing import Any, Dict


STOCK_ANALYSIS_PROMPT = """당신은 한국 주식 시장을 전문으로 하는 애널리스트입니다.
아래 데이터를 분석하여 투자 점수와 상세 분석을 JSON 형식으로 작성해주세요.

{stock_data}

## 평가 기준

다음 6개 카테고리에 대해 점수를 부여하세요. 각 카테고리별 만점과 평가 기준은 다음과 같습니다:

### 1. 기술적 분석 (25점 만점)
- **추세 (6점)**: MA20 > MA60이면 상승 추세. 차이가 클수록 강한 추세.
- **RSI (6점)**: 50-70 구간이 최적(건강한 상승). 70-80 강한 모멘텀. 30 이하/80 이상은 주의.
- **지지/저항 (6점)**: 52주 내 위치가 높을수록 상승 추세. 90% 이상은 신고가 근처.
- **MACD (4점)**: MACD > Signal이면 상승 신호. 둘 다 양수면 더 강함.
- **ADX (3점)**: 30 이상 강한 추세, 20 이하 추세 없음(횡보).

### 2. 수급 분석 (20점 만점)
- **외국인 (8점)**: 최근 5일 순매수 일수와 누적 금액. 연속 3일 이상 순매수면 긍정적.
- **기관 (8점)**: 최근 5일 순매수 일수와 누적 금액. 연속 3일 이상 순매수면 긍정적.
- **거래대금 (4점)**: 평균 대비 거래량 증가는 관심도 상승 의미.

### 3. 펀더멘털 분석 (20점 만점)
- **PER (4점)**: 섹터 평균 대비 저평가면 높은 점수. 적자(음수)는 0점.
- **PBR (4점)**: 섹터 평균 대비 저평가면 높은 점수. 1 이하면 자산가치 대비 저평가.
- **ROE (4점)**: 15% 이상 양호, 20% 이상 우수, 30% 이상 최우수.
- **성장률 (5점)**: 영업이익 YoY 성장률. 30% 이상 고성장, 100% 이상 폭발적 성장.
- **부채비율 (3점)**: 100% 이하 건전, 200% 이상 위험.

### 4. 시장 환경 (15점 만점)
- **뉴스 센티먼트 (7.5점)**: 긍정 뉴스 비중과 평균 센티먼트 점수 반영.
- **섹터 모멘텀 (3.75점)**: 해당 섹터의 최근 추세와 시장 관심도.
- **애널리스트 전망 (3.75점)**: 목표가 대비 현재가 괴리율. 상승 여력이 클수록 좋음.

### 5. 리스크 평가 (10점 만점)
- **변동성 (4점)**: ATR%가 2% 이하면 안정, 5% 이상이면 고위험.
- **베타 (3점)**: 0.8~1.2면 시장과 유사, 1.5 이상이면 고위험.
- **하방리스크 (3점)**: 최대낙폭 10% 이하면 안정, 30% 이상이면 고위험.

### 6. 상대 강도 (10점 만점)
- **섹터 내 순위 (5점)**: 섹터 내에서 상위 순위일수록 높은 점수.
- **시장 대비 알파 (5점)**: 20일 수익률이 시장 대비 초과 수익이면 높은 점수.

## 투자 등급 기준
- **Strong Buy**: 80점 이상
- **Buy**: 60~79점
- **Hold**: 40~59점
- **Sell**: 20~39점
- **Strong Sell**: 20점 미만

## JSON 응답 형식

다음 JSON 스키마에 맞춰 응답하세요. 각 필드를 빠짐없이 채워주세요:

```json
{
  "total_score": <0-100 사이 총점>,
  "grade": "<Strong Buy|Buy|Hold|Sell|Strong Sell>",
  "categories": {
    "technical": {
      "score": <0-25>,
      "reasoning": "<기술적 분석 판단 근거를 1-2문장으로>"
    },
    "supply": {
      "score": <0-20>,
      "reasoning": "<수급 분석 판단 근거를 1-2문장으로>"
    },
    "fundamental": {
      "score": <0-20>,
      "reasoning": "<펀더멘털 분석 판단 근거를 1-2문장으로>"
    },
    "market": {
      "score": <0-15>,
      "reasoning": "<시장 환경 판단 근거를 1-2문장으로>"
    },
    "risk": {
      "score": <0-10>,
      "reasoning": "<리스크 평가 판단 근거를 1-2문장으로>"
    },
    "relative_strength": {
      "score": <0-10>,
      "reasoning": "<상대 강도 판단 근거를 1-2문장으로>"
    }
  },
  "summary": "<핵심 투자 테마를 1-2문장으로 강렬하게 요약>",
  "financial_analysis": "<재무 & 밸류에이션 분석을 2-3문장으로. 마크다운 사용 가능>",
  "technical_analysis": "<기술적 & 차트 분석을 2-3문장으로. 마크다운 사용 가능>",
  "market_sentiment": "<뉴스 & 시장 센티먼트 분석을 2-3문장으로. 마크다운 사용 가능>",
  "comprehensive_analysis": "<종합 투자 의견을 3-5문장으로. 마크다운 사용 가능>",
  "investment_thesis": ["<투자 포인트 1>", "<투자 포인트 2>", "<투자 포인트 3>"],
  "risks": ["<리스크 요인 1>", "<리스크 요인 2>"]
}
```

## 분석 가이드라인

1. **객관적 데이터 기반**: 제공된 데이터를 근거로 판단하세요. 추측하지 마세요.
2. **섹터 특성 반영**: 해당 섹터의 특성과 현재 시장 상황을 고려하세요.
3. **균형 잡힌 시각**: 강점과 약점을 모두 언급하세요.
4. **구체적 수치 인용**: 분석에서 데이터 수치를 인용하여 근거를 명확히 하세요.
5. **전문가 어조**: 투자 자문사 수준의 전문적인 보고서 어조를 유지하세요.

JSON만 응답하세요. 다른 설명은 불필요합니다."""


def build_stock_analysis_prompt(stock_data_context: str) -> str:
    """
    주식 분석 프롬프트를 생성합니다.

    Args:
        stock_data_context: StockDataBundle.to_prompt_context()의 출력

    Returns:
        완성된 프롬프트 문자열
    """
    return STOCK_ANALYSIS_PROMPT.format(stock_data=stock_data_context)
